// Activity 2 : While Loop

// Task 3: Write a program to calculate the sum of numbers from 1 to 10 using a while loop

let i = 1;
let total = 0;

while (i <= 10) {
  total += i;
  i++;
}

console.log(total);

// Task 4 : Write a program to print numbers from 10 to 1 using a while loop

let z = 10;

while (z >= 1) {
  console.log(z);
  z--;
}
