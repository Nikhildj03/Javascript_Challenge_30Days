// Activity 1  For Loop

// Task 1 : Write a Program to print the Numbers from 1 to 10 using a for loop.

for (let index = 1; index <= 10; index++) {
  console.log(index);
}

// Task 2 : Write a program to print the Multiplication table of 5 using a for loop

for (let i = 1; i <= 10; i++) {
  console.log(`5 * ${i} = ${5 * i}`);
}

